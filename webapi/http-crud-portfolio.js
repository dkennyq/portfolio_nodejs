import { DynamoDBClient } from "@aws-sdk/client-dynamodb";
import {
  DynamoDBDocumentClient,
  ScanCommand,
  PutCommand,
  GetCommand,
  DeleteCommand,
} from "@aws-sdk/lib-dynamodb";

const client = new DynamoDBClient({});

const dynamo = DynamoDBDocumentClient.from(client);

const tableName = "zem_user_portfolio";

export const handler = async (event, context) => {
  let body;
  let statusCode = 200;
  const headers = {
    "Content-Type": "application/json",
  };

  try {
    switch (event.routeKey) {
      case "DELETE /items/{id}":
        await dynamo.send(
          new DeleteCommand({
            TableName: tableName,
            Key: {
              id_user_portfolio: parseInt(event.pathParameters.id, 10),
            },
          })
        );
        body = `Deleted item ${event.pathParameters.id}`;
        break;
      case "GET /items/{id}":
        body = await dynamo.send(
          new GetCommand({
            TableName: tableName,
            Key: {
              id_user_portfolio: parseInt(event.pathParameters.id, 10),
            },
          })
        );
        body = body.Item;
        break;
      case "GET /items":
        body = await dynamo.send(
          new ScanCommand({ TableName: tableName })
        );
        body = body.Items;
        break;
      case "PUT /items":
        let requestJSON = JSON.parse(event.body);
        await dynamo.send(
          new PutCommand({
            TableName: tableName,
            Item: {
              id_user_portfolio: requestJSON.id_user_portfolio,
              full_name: requestJSON.full_name,
              last_name: requestJSON.last_name,
              phone: requestJSON.phone,
              portfolio_description: requestJSON.portfolio_description,
              portfolio_summary: requestJSON.portfolio_summary,
              profile_image: requestJSON.profile_image,
              user_email: requestJSON.user_email,
              x_user_name: requestJSON.x_user_name,
              first_name: requestJSON.first_name,
              professional_summary: requestJSON.professional_summary
            },
          })
        );
        body = `Put item ${requestJSON.id_user_portfolio}`;
        break;
      default:
        throw new Error(`Unsupported route: "${event.routeKey}"`);
    }
  } catch (err) {
    statusCode = 400;
    body = err.message;
  } finally {
    body = JSON.stringify(body);
  }

  return {
    statusCode,
    body,
    headers,
  };
};
